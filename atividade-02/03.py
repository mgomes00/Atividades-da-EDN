# Dados do problema
nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

# Cálculo da média
media = (nota1 + nota2 + nota3) / 3

# Exibição dos resultados
print("--- Calculadora de Média Escolar ---")
print(f"Nota 1: {nota1:.1f}")
print(f"Nota 2: {nota2:.1f}")
print(f"Nota 3: {nota3:.1f}")
print("-" * 35)
print(f"Média Final: {media:.2f}")